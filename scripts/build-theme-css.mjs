import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const themeJsonPath = path.join(rootDir, 'theme.json');
const themeCssPath = path.join(rootDir, 'theme.css');
const checkOnly = process.argv.includes('--check');

const themeData = JSON.parse(await readFile(themeJsonPath, 'utf8'));

function fail(message) {
  console.error(message);
  process.exit(1);
}

function kebabCase(value) {
  return value.replace(/[A-Z]/g, (match) => `-${match.toLowerCase()}`);
}

function assertObject(value, label) {
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    fail(`${label} must be an object.`);
  }
}

function validateThemeData(data) {
  assertObject(data, 'theme.json');

  if (data.version !== 1) {
    fail('theme.json version must be 1.');
  }

  if (!Array.isArray(data.tokens) || data.tokens.length === 0) {
    fail('theme.json tokens must be a non-empty array.');
  }

  const tokenSet = new Set();
  for (const token of data.tokens) {
    if (typeof token !== 'string' || !/^[a-z][a-zA-Z0-9]*$/.test(token)) {
      fail(`Invalid token name: ${token}`);
    }
    if (tokenSet.has(token)) {
      fail(`Duplicate token name: ${token}`);
    }
    tokenSet.add(token);
  }

  assertObject(data.themes, 'theme.json themes');
  assertObject(data.tokenDefaults, 'theme.json tokenDefaults');

  for (const mode of ['light', 'medium', 'dark']) {
    assertObject(data.tokenDefaults[mode], `tokenDefaults ${mode}`);

    for (const token of data.tokens) {
      if (typeof data.tokenDefaults[mode][token] !== 'string' || data.tokenDefaults[mode][token].length === 0) {
        fail(`tokenDefaults ${mode} is missing token ${token}.`);
      }
    }

    for (const token of Object.keys(data.tokenDefaults[mode])) {
      if (!tokenSet.has(token)) {
        fail(`tokenDefaults ${mode} has unknown token ${token}.`);
      }
    }
  }

  for (const [themeId, theme] of Object.entries(data.themes)) {
    if (!/^[a-z][a-z0-9-]*$/.test(themeId)) {
      fail(`Invalid theme id: ${themeId}`);
    }
    assertObject(theme, `theme ${themeId}`);

    if (typeof theme.label !== 'string' || theme.label.length === 0) {
      fail(`Theme ${themeId} must have a label.`);
    }

    if (!['light', 'medium', 'dark'].includes(theme.mode)) {
      fail(`Theme ${themeId} mode must be light, medium, or dark.`);
    }

    if (!['light', 'dark'].includes(theme.colorScheme)) {
      fail(`Theme ${themeId} colorScheme must be light or dark.`);
    }

    assertObject(theme.colors, `theme ${themeId} colors`);

    for (const token of Object.keys(theme.colors)) {
      if (!tokenSet.has(token)) {
        fail(`Theme ${themeId} has unknown token ${token}.`);
      }
    }

    const resolvedColors = { ...data.tokenDefaults[theme.mode], ...theme.colors };
    for (const token of data.tokens) {
      if (typeof resolvedColors[token] !== 'string' || resolvedColors[token].length === 0) {
        fail(`Theme ${themeId} is missing resolved token ${token}.`);
      }
    }
  }

  assertObject(data.aliases, 'theme.json aliases');

  for (const [alias, target] of Object.entries(data.aliases)) {
    if (!data.themes[target]) {
      fail(`Alias ${alias} points to missing theme ${target}.`);
    }
  }
}

function cssBlock(selectors, theme, tokens) {
  const lines = [
    `${selectors.join(',\n')} {`,
    `  color-scheme: ${theme.colorScheme};`,
  ];

  for (const token of tokens) {
    lines.push(`  --theme-${kebabCase(token)}: ${theme.resolvedColors[token]};`);
  }

  lines.push('}');
  return lines.join('\n');
}

function buildCss(data) {
  const blocks = [
    '/* This file is generated by scripts/build-theme-css.mjs. Do not edit directly. */',
    '',
  ];

  for (const [themeId, theme] of Object.entries(data.themes)) {
    const resolvedTheme = {
      ...theme,
      resolvedColors: { ...data.tokenDefaults[theme.mode], ...theme.colors },
    };
    const selectors =
      themeId === 'light'
        ? [':root', ":root[data-theme='light']", "[data-theme='light']"]
        : [`:root[data-theme='${themeId}']`, `[data-theme='${themeId}']`];

    blocks.push(cssBlock(selectors, resolvedTheme, data.tokens), '');
  }

  for (const [alias, target] of Object.entries(data.aliases)) {
    const theme = data.themes[target];
    const resolvedTheme = {
      ...theme,
      resolvedColors: { ...data.tokenDefaults[theme.mode], ...theme.colors },
    };
    blocks.push(cssBlock([`:root[data-theme='${alias}']`, `[data-theme='${alias}']`], resolvedTheme, data.tokens), '');
  }

  return `${blocks.join('\n').trimEnd()}\n`;
}

validateThemeData(themeData);

const nextCss = buildCss(themeData);

if (checkOnly) {
  let currentCss = '';
  try {
    currentCss = await readFile(themeCssPath, 'utf8');
  } catch (error) {
    if (error.code !== 'ENOENT') {
      throw error;
    }
  }

  if (currentCss !== nextCss) {
    fail('theme.css is stale. Run npm run build and commit the result.');
  }

  console.log('theme.css is up to date.');
} else {
  await writeFile(themeCssPath, nextCss);
  console.log('Generated theme.css.');
}
